using PayFlow.Application.UseCases;
using PayFlow.Domain.Interfaces;
using PayFlow.Infrastructure.Providers;

public class Program
{
    public static void Main(string[] args)
    {
        var builder = WebApplication.CreateBuilder(args);

        // Add services to the container.
        builder.Services.AddControllers();

        // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
        builder.Services.AddEndpointsApiExplorer();
        builder.Services.AddSwaggerGen();


        builder.Services.AddSingleton<IPaymentProvider, FastPayProvider>();
        builder.Services.AddSingleton<IPaymentProvider, SecurePayProvider>();
        builder.Services.AddScoped<IProcessPaymentUseCase, ProcessPaymentUseCase>();


        var app = builder.Build();

        // Configure the HTTP request pipeline.
        if (app.Environment.IsDevelopment())
        {
            app.UseSwagger();
            app.UseSwaggerUI();
        }

        app.UseHttpsRedirection();
        app.UseAuthorization();
        app.MapControllers();
        app.Run();
    }
}